# A systematic strategy to apply for solving problem:
# 1. State the problem clearly. Indentify the input & output formats.
# 2. Come up with some example inputs  & outputs. Try to cover all edge cases.
# 3. Come up with a correct solution for the problem. State it in plain English.
# 4. Implement the solution and test it using example inputs. Fix bugs, if any.
# 5. Analyze the algorithm's complexity and identify ineffeciencies, if any.
# 6. Apply the right technique to overcome the inefficiency. Repeat step 3 to 6.

